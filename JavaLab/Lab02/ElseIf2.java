public class ElseIf2 {
    public static void main(String[] args) {
        int number = 10;
        if (number > 10) {
            System.out.println("Greater than 10");
        } else if (number < 10) {
            System.out.println("Less than 10");
        } else {
            System.out.println("Equal to 10");
        }
    }
}
